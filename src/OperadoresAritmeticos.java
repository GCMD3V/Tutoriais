public class OperadoresAritmeticos {
    public static void main(String[] args) {
        // + Adição - Concatenação 'String'
        // - Subtração
        // * Multiplicação
        // / Divisão
        // % Resto

        String textoA = "Bom ";
        String textoB = "Dia";

        double valorA = 5;
        double valorB = 7;

        System.out.println(textoA + textoB);
        System.out.println(valorA / valorB);

        System.out.println( (valorA + valorB) / 2 );
    }
}
